"""Formatando valores
:s - Texto
:d - Inteiros
:f - ponto flutuante
:.quantidade de casas decimais
:(caracter) (> ou < ou ^) quantidade """


# :(caracter) (> ou < ou ^) quantidade
num = 1
print(num)
print(f'{num:.^10}')
print(f'{num:.<10}')
print(f'{num:.>10}')